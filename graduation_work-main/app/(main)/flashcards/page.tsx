export default async function FlashcardsPage() {
  const userProgress = await getUserProgress(); 
  // Отримуємо прогрес користувача з БД

  const hasProgress = !!userProgress?.activeRegion; 
  // true, якщо користувач обрав регіон і почав навчання

  return (
    <div className="flex flex-row-reverse gap-12 px-6 py-8">
      {/* 
        flex-row-reverse → контент і бокове меню поміняні місцями
        gap-12 → відстань між блоками
        px-6 py-8 → відступи
      */}

      <StickyWrapper>
        {/* Лівий "липкий" блок */}
        {userProgress && (
          <>
            <UserProgress
              activeCourse={userProgress.activeRegion} 
              hearts={userProgress.hearts} 
              points={userProgress.points} 
            />
            {/* Відображає прогрес користувача */}

            <Quests points={userProgress.points} />
            {/* Відображає квести */}

            {userProgress.activeRegion && (
              <RegionImage activeRegionId={userProgress.activeRegion.id} />
              /* Відображає картинку поточного регіону */
            )}
          </>
        )}
        <Feedback />
        {/* Форма для відгуків */}
      </StickyWrapper>

      <FeedWrapper>
        {/* Правий основний блок */}
        <FlashcardsPracticeClient
          flashcards={flashcards} 
          hasProgress={hasProgress} 
          /* Передаємо картки та чи користувач має прогрес */
        />
      </FeedWrapper>
    </div>
  );
}